//2. C program to find area of a triangle if base and height are give

#include<stdio.h>
void main()
{
 int a,b,c,area;
 printf("Enter the Base of a triangle:-");
 scanf("%d",&a);
 printf("Enter the Height of a triangle:-");
 scanf("%d",&b);
 c=a*b;
 area=c/2;
 printf("Area= %d",area);
 }
